    protected function addLine(string $line = '', int $indent = 0): void
    {
        if ($this->currentBasicBlock !== null) {
            $this->currentBasicBlock->addLine(new IRLine($line, $indent));
        } else {
            $this->module->addLine(new IRLine($line));
        }
    }
}

