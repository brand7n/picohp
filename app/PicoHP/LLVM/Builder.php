    /**
     * @param array<ValueAbstract> $operands
     */
    public function createInstruction(string $opcode, array $operands, bool $emitResult = true, BaseType $resultType = BaseType::INT): ValueAbstract
    {
        \App\PicoHP\CompilerInvariant::check(count($operands) > 0);
        $type = $operands[0]->getType();
        $operandString = implode(', ', array_map(
            static fn ($operand): string => $operand->render(),
            $operands,
        ));
        $resultVal = new Void_();
        if ($emitResult) {
            $resultVal = new Instruction($opcode, $resultType);
            $this->addLine("{$resultVal->render()} = {$opcode} {$type->toLLVM()} {$operandString}", 1);
        } else {
            $this->addLine("{$opcode} {$type->toLLVM()} {$operandString}", 1);
        }
        return $resultVal;
    }
    /**
     * @param array<ValueAbstract> $operands
     */
    public function createBranch(array $operands): ValueAbstract
    {
        if (count($operands) === 1) {
            \App\PicoHP\CompilerInvariant::check($operands[0] instanceof Label);
            $this->addLine("br label {$operands[0]->render()}", 1);
        } elseif (count($operands) === 3) {
            \App\PicoHP\CompilerInvariant::check($operands[1] instanceof Label && $operands[2] instanceof Label);
            $this->addLine("br i1 {$operands[0]->render()}, label {$operands[1]->render()}, label {$operands[2]->render()}", 1);
        } else {
            throw new \RuntimeException('Invalid branch operands');
        }
        return new Void_();
    }

    public function createAlloca(string $name, BaseType $type): ValueAbstract
    {
        $resultVal = new AllocaInst($name, $type);
        $this->addLine("{$resultVal->render()} = alloca {$type->toLLVM()}", 1);
        return $resultVal;
    }

    /**
     * Emit an alloca in the entry block of the given function, ensuring it dominates all uses.
     */
    public function createEntryAlloca(Function_ $function, string $name, BaseType $type): ValueAbstract
    {
        $resultVal = new AllocaInst($name, $type);
        $blocks = $function->getBasicBlocks();
        \App\PicoHP\CompilerInvariant::check(count($blocks) > 0);
        $blocks[0]->insertAtStart(new IRLine("    {$resultVal->render()} = alloca {$type->toLLVM()}"));
        return $resultVal;
    }

