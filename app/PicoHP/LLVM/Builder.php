    public function createCallPrintf(ValueAbstract $val): ValueAbstract
    {
        if ($val->getType() === BaseType::PTR || $val->getType() === BaseType::STRING) {
            $this->addLine("call i32 (ptr, ...) @printf(ptr @.str.s, ptr {$val->render()})", 1);
        } elseif ($val->getType() === BaseType::FLOAT) {
            $this->addLine("call i32 (ptr, ...) @printf(ptr @.str.f, {$val->getType()->toLLVM()} {$val->render()})", 1);
        } else {
            $this->addLine("call i32 (ptr, ...) @printf(ptr @.str.d, {$val->getType()->toLLVM()} {$val->render()})", 1);
        }
        return new Void_();
    }

    /**
     * @param array<ValueAbstract> $paramVals
     */
    public function createCall(string $functionName, array $paramVals, BaseType $returnType): ValueAbstract
    {
        $paramString = implode(', ', array_map(
            static fn ($param): string => "{$param->getType()->toLLVM()} {$param->render()}",
            $paramVals,
        ));
        if ($returnType === BaseType::VOID) {
            $this->addLine("call void @{$functionName} ({$paramString})", 1);
            return new Void_();
        }
        $returnVal = new Instruction('call', $returnType);
        $this->addLine("{$returnVal->render()} = call {$returnType->toLLVM()} @{$functionName} ({$paramString})", 1);
        return $returnVal;
    }

