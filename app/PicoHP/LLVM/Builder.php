    public function createStringConstant(string $value): ValueAbstract
    {
        $count = $this->stringConstantCount++;
        $name = ".str.lit.{$count}";
        $escaped = $this->escapeStringForLLVM($value);
        $len = strlen($value) + 1; // +1 for null terminator
        $this->module->addLine(new IRLine("@{$name} = private constant [{$len} x i8] c\"{$escaped}\\00\", align 1"));
        return new Global_($name, BaseType::STRING);
    }

    protected function escapeStringForLLVM(string $value): string
    {
        $result = '';
        for ($i = 0; $i < strlen($value); $i++) {
            $ch = $value[$i];
            $ord = ord($ch);
            if ($ord < 32 || $ord > 126 || $ch === '"' || $ch === '\\') {
                $result .= '\\' . strtoupper(str_pad(dechex($ord), 2, '0', STR_PAD_LEFT));
            } else {
                $result .= $ch;
            }
        }
        return $result;
    }

    public function createStringConcat(ValueAbstract $left, ValueAbstract $right): ValueAbstract
    {
        $resultVal = new Instruction('concat', BaseType::PTR);
        $this->addLine("{$resultVal->render()} = call ptr @pico_string_concat(ptr {$left->render()}, ptr {$right->render()})", 1);
        return $resultVal;
    }

    public function createCallPrintf(ValueAbstract $val): ValueAbstract
    {
        if ($val->getType() === BaseType::PTR || $val->getType() === BaseType::STRING) {
            $this->addLine("call i32 (ptr, ...) @printf(ptr @.str.s, ptr {$val->render()})", 1);
        } elseif ($val->getType() === BaseType::FLOAT) {
            $this->addLine("call i32 (ptr, ...) @printf(ptr @.str.f, {$val->getType()->toLLVM()} {$val->render()})", 1);
        } else {
            $this->addLine("call i32 (ptr, ...) @printf(ptr @.str.d, {$val->getType()->toLLVM()} {$val->render()})", 1);
        }
        return new Void_();
    }

    /**
     * @param array<ValueAbstract> $paramVals
     */
    public function createCall(string $functionName, array $paramVals, BaseType $returnType): ValueAbstract
    {
        $paramString = (new Collection($paramVals))
            ->map(fn ($param): string => "{$param->getType()->toLLVM()} {$param->render()}")
            ->join(', ');
        if ($returnType === BaseType::VOID) {
            $this->addLine("call void @{$functionName} ({$paramString})", 1);
            return new Void_();
        }
        $returnVal = new Instruction('call', $returnType);
        $this->addLine("{$returnVal->render()} = call {$returnType->toLLVM()} @{$functionName} ({$paramString})", 1);
        return $returnVal;
    }

    public function createGetElementPtr(ValueAbstract $var, ValueAbstract $dim): ValueAbstract
    {
        $arrayType = $var->getType();
        $resultVal = new Instruction('getelementptr', BaseType::PTR);
        $this->addLine("{$resultVal->render()} = getelementptr inbounds {$arrayType->toLLVM()}, ptr {$var->render()}, i64 0, {$dim->getType()->toLLVM()} {$dim->render()}", 1);
        return $resultVal;
    }

