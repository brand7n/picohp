    /**
     * Emit a call to picohp_unimplemented(name) + unreachable.
     * Prints the function name to stderr before aborting.
     */
    /**
     * Emit a call to picohp_unimplemented(name) + unreachable, then throw
     * to unwind out of the current function body compilation (caught by
     * the try/catch in BuildStmtTrait which stubs the function).
     */
    public function emitUnimplementedAbort(string $funcName): void
    {
        $nameConst = $this->createStringConstant($funcName);
        $this->addLine("call void @picohp_unimplemented(ptr {$nameConst->render()})", 1);
        $this->addLine('unreachable', 1);
    }

    public function createStringConstant(string $value): ValueAbstract
    {
        $count = $this->stringConstantCount++;
        $name = ".str.lit.{$count}";
        $escaped = $this->escapeStringForLLVM($value);
        $len = strlen($value) + 1; // +1 for null terminator
        $this->module->addLine(new IRLine("@{$name} = private constant [{$len} x i8] c\"{$escaped}\\00\", align 1"));
        return new Global_($name, BaseType::STRING);
    }

    protected function escapeStringForLLVM(string $value): string
    {
        $result = '';
        for ($i = 0; $i < strlen($value); $i++) {
            $ch = $value[$i];
            $ord = ord($ch);
            if ($ord < 32 || $ord > 126 || $ch === '"' || $ch === '\\') {
                $result .= '\\' . strtoupper(str_pad(dechex($ord), 2, '0', STR_PAD_LEFT));
            } else {
                $result .= $ch;
            }
        }
        return $result;
    }

