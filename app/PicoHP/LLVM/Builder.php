<?php

declare(strict_types=1);

namespace App\PicoHP\LLVM;

use App\PicoHP\{BaseType};
use Illuminate\Support\Collection;
use App\PicoHP\LLVM\Value\{Instruction, Void_, AllocaInst, Global_, Label};

class Builder
{
    protected ?BasicBlock $currentBasicBlock = null;
    protected Module $module;
    protected int $stringConstantCount = 0;

    public function __construct(Module $module, string $triple, string $layout)
    {
        $this->module = $module;

        $this->addLine('; generated by picoHP');
        $this->addLine("; ModuleID = '{$this->module->getName()}'");
        $this->addLine("source_filename = \"{$this->module->getName()}\"");
        $this->addLine('target datalayout = "' . $layout . '"');
        $this->addLine('target triple = "' . $triple . '"');
        $this->addLine();
        $this->addLine('@.str.d = private constant [3 x i8] c"%d\00", align 1');
        $this->addLine('@.str.f = private constant [6 x i8] c"%.14g\00", align 1');
        $this->addLine('@.str.s = private constant [3 x i8] c"%s\00", align 1');
        $this->addLine();
        $this->addLine('declare i32 @printf(ptr, ...)');
        $this->addLine('declare ptr @pico_string_concat(ptr, ptr)');
        $this->addLine('declare i32 @pico_rt_version()');
        $this->addLine();
        $this->addLine('; array runtime');
        $this->addLine('declare ptr @pico_array_new()');
        $this->addLine('declare i32 @pico_array_len(ptr)');
        $this->addLine('declare void @pico_array_push_int(ptr, i32)');
        $this->addLine('declare void @pico_array_push_float(ptr, double)');
        $this->addLine('declare void @pico_array_push_bool(ptr, i32)');
        $this->addLine('declare void @pico_array_push_str(ptr, ptr)');
        $this->addLine('declare i32 @pico_array_get_int(ptr, i32)');
        $this->addLine('declare double @pico_array_get_float(ptr, i32)');
        $this->addLine('declare i32 @pico_array_get_bool(ptr, i32)');
        $this->addLine('declare ptr @pico_array_get_str(ptr, i32)');
        $this->addLine('declare void @pico_array_set_int(ptr, i32, i32)');
        $this->addLine('declare void @pico_array_set_float(ptr, i32, double)');
        $this->addLine('declare void @pico_array_set_bool(ptr, i32, i32)');
        $this->addLine('declare void @pico_array_set_str(ptr, i32, ptr)');
    }

    public function setInsertPoint(BasicBlock $bb): void
    {
        $this->currentBasicBlock = $bb;
    }

    /**
     * @param array<ValueAbstract> $operands
     */
    public function createInstruction(string $opcode, array $operands, bool $emitResult = true, BaseType $resultType = BaseType::INT): ValueAbstract
    {
        assert(count($operands) > 0);
        $type = $operands[0]->getType();
        $operandString = (new Collection($operands))
            ->map(fn ($operand): string => $operand->render())
            ->join(', ');
        $resultVal = new Void_();
        if ($emitResult) {
            $resultVal = new Instruction($opcode, $resultType);
            $this->addLine("{$resultVal->render()} = {$opcode} {$type->toLLVM()} {$operandString}", 1);
        } else {
            $this->addLine("{$opcode} {$type->toLLVM()} {$operandString}", 1);
        }
        return $resultVal;
    }
    /**
     * @param array<ValueAbstract> $operands
     */
    public function createBranch(array $operands): ValueAbstract
    {
        if (count($operands) === 1) {
            assert($operands[0] instanceof Label);
            $this->addLine("br label {$operands[0]->render()}", 1);
        } elseif (count($operands) === 3) {
            assert($operands[1] instanceof Label && $operands[2] instanceof Label);
            $this->addLine("br i1 {$operands[0]->render()}, label {$operands[1]->render()}, label {$operands[2]->render()}", 1);
        } else {
            throw new \RuntimeException('Invalid branch operands');
        }
        return new Void_();
    }

    public function createAlloca(string $name, BaseType $type): ValueAbstract
    {
        $resultVal = new AllocaInst($name, $type);
        $this->addLine("{$resultVal->render()} = alloca {$type->toLLVM()}", 1);
        return $resultVal;
    }

    public function createLoad(ValueAbstract $loadptr): ValueAbstract
    {
        $type = $loadptr->getType();
        $resultVal = new Instruction("{$loadptr->getName()}_load", $type);
        $this->addLine("{$resultVal->render()} = load {$type->toLLVM()}, ptr {$loadptr->render()}", 1);
        return $resultVal;
    }

    public function createStore(ValueAbstract $rval, ValueAbstract $lval): ValueAbstract
    {
        // TODO: in case of i8* lval should we cast $rval from i32
        //       use u8 instead?
        //assert($lval instanceof AllocaInst || ($lval instanceof Instruction && $lval->getType() === 'i8*'));
        $type = $rval->getType();
        $typeStr = $type->toLLVM();
        $ptrType = $typeStr === 'ptr' ? 'ptr' : "{$typeStr}*";
        $this->addLine("store {$typeStr} {$rval->render()}, {$ptrType} {$lval->render()}", 1);
        return new Void_();
    }

    public function createFpToSi(ValueAbstract $val): ValueAbstract
    {
        $resultVal = new Instruction("cast", BaseType::INT);
        $this->addLine("{$resultVal->render()} = fptosi double {$val->render()} to i32", 1);
        return $resultVal;
    }

    public function createSiToFp(ValueAbstract $val): ValueAbstract
    {
        $resultVal = new Instruction("cast", BaseType::FLOAT);
        $this->addLine("{$resultVal->render()} = sitofp {$val->getType()->toLLVM()} {$val->render()} to double", 1);
        return $resultVal;
    }

    public function createZext(ValueAbstract $val): ValueAbstract
    {
        $resultVal = new Instruction("cast", BaseType::INT);
        $this->addLine("{$resultVal->render()} = zext i1 {$val->render()} to i32", 1);
        return $resultVal;
    }

    public function createStringConstant(string $value): ValueAbstract
    {
        $count = $this->stringConstantCount++;
        $name = ".str.lit.{$count}";
        $escaped = $this->escapeStringForLLVM($value);
        $len = strlen($value) + 1; // +1 for null terminator
        $this->module->addLine(new IRLine("@{$name} = private constant [{$len} x i8] c\"{$escaped}\\00\", align 1"));
        return new Global_($name, BaseType::STRING);
    }

    protected function escapeStringForLLVM(string $value): string
    {
        $result = '';
        for ($i = 0; $i < strlen($value); $i++) {
            $ch = $value[$i];
            $ord = ord($ch);
            if ($ord < 32 || $ord > 126 || $ch === '"' || $ch === '\\') {
                $result .= '\\' . strtoupper(str_pad(dechex($ord), 2, '0', STR_PAD_LEFT));
            } else {
                $result .= $ch;
            }
        }
        return $result;
    }

    public function createStringConcat(ValueAbstract $left, ValueAbstract $right): ValueAbstract
    {
        $resultVal = new Instruction('concat', BaseType::PTR);
        $this->addLine("{$resultVal->render()} = call ptr @pico_string_concat(ptr {$left->render()}, ptr {$right->render()})", 1);
        return $resultVal;
    }

    public function createCallPrintf(ValueAbstract $val): ValueAbstract
    {
        if ($val->getType() === BaseType::PTR || $val->getType() === BaseType::STRING) {
            $this->addLine("call i32 (ptr, ...) @printf(ptr @.str.s, ptr {$val->render()})", 1);
        } elseif ($val->getType() === BaseType::FLOAT) {
            $this->addLine("call i32 (ptr, ...) @printf(ptr @.str.f, {$val->getType()->toLLVM()} {$val->render()})", 1);
        } else {
            $this->addLine("call i32 (ptr, ...) @printf(ptr @.str.d, {$val->getType()->toLLVM()} {$val->render()})", 1);
        }
        return new Void_();
    }

    /**
     * @param array<ValueAbstract> $paramVals
     */
    public function createCall(string $functionName, array $paramVals, BaseType $returnType): ValueAbstract
    {
        $paramString = (new Collection($paramVals))
            ->map(fn ($param): string => "{$param->getType()->toLLVM()} {$param->render()}")
            ->join(', ');
        if ($returnType === BaseType::VOID) {
            $this->addLine("call void @{$functionName} ({$paramString})", 1);
            return new Void_();
        }
        $returnVal = new Instruction('call', $returnType);
        $this->addLine("{$returnVal->render()} = call {$returnType->toLLVM()} @{$functionName} ({$paramString})", 1);
        return $returnVal;
    }

    public function createGetElementPtr(ValueAbstract $var, ValueAbstract $dim): ValueAbstract
    {
        $arrayType = $var->getType();
        $resultVal = new Instruction('getelementptr', BaseType::PTR);
        $this->addLine("{$resultVal->render()} = getelementptr inbounds {$arrayType->toLLVM()}, ptr {$var->render()}, i64 0, {$dim->getType()->toLLVM()} {$dim->render()}", 1);
        return $resultVal;
    }

    public function createNullCheck(ValueAbstract $val): ValueAbstract
    {
        $resultVal = new Instruction('null_check', BaseType::BOOL);
        $this->addLine("{$resultVal->render()} = icmp eq ptr {$val->render()}, null", 1);
        return $resultVal;
    }

    public function createSelect(ValueAbstract $cond, ValueAbstract $true, ValueAbstract $false): ValueAbstract
    {
        $type = $true->getType();
        $resultVal = new Instruction('select', $type);
        $this->addLine("{$resultVal->render()} = select i1 {$cond->render()}, {$type->toLLVM()} {$true->render()}, {$type->toLLVM()} {$false->render()}", 1);
        return $resultVal;
    }

    // -- dynamic array runtime calls -----------------------------------------

    public function createArrayNew(): ValueAbstract
    {
        $resultVal = new Instruction('array_new', BaseType::PTR);
        $this->addLine("{$resultVal->render()} = call ptr @pico_array_new()", 1);
        return $resultVal;
    }

    public function createArrayLen(ValueAbstract $arr): ValueAbstract
    {
        $resultVal = new Instruction('array_len', BaseType::INT);
        $this->addLine("{$resultVal->render()} = call i32 @pico_array_len(ptr {$arr->render()})", 1);
        return $resultVal;
    }

    public function createArrayPush(ValueAbstract $arr, ValueAbstract $val, BaseType $elementType): void
    {
        $suffix = $this->arrayFuncSuffix($elementType);
        $llvmType = $this->arrayArgType($elementType);
        $this->addLine("call void @pico_array_push_{$suffix}(ptr {$arr->render()}, {$llvmType} {$val->render()})", 1);
    }

    public function createArrayGet(ValueAbstract $arr, ValueAbstract $idx, BaseType $elementType): ValueAbstract
    {
        $suffix = $this->arrayFuncSuffix($elementType);
        $retType = $this->arrayArgType($elementType);
        $resultVal = new Instruction('array_get', $elementType);
        $this->addLine("{$resultVal->render()} = call {$retType} @pico_array_get_{$suffix}(ptr {$arr->render()}, i32 {$idx->render()})", 1);
        return $resultVal;
    }

    public function createArraySet(ValueAbstract $arr, ValueAbstract $idx, ValueAbstract $val, BaseType $elementType): void
    {
        $suffix = $this->arrayFuncSuffix($elementType);
        $llvmType = $this->arrayArgType($elementType);
        $this->addLine("call void @pico_array_set_{$suffix}(ptr {$arr->render()}, i32 {$idx->render()}, {$llvmType} {$val->render()})", 1);
    }

    private function arrayFuncSuffix(BaseType $type): string
    {
        return match ($type) {
            BaseType::INT => 'int',
            BaseType::FLOAT => 'float',
            BaseType::BOOL => 'bool',
            BaseType::STRING, BaseType::PTR => 'str',
            default => throw new \RuntimeException("unsupported array element type: {$type->value}"),
        };
    }

    private function arrayArgType(BaseType $type): string
    {
        return match ($type) {
            BaseType::BOOL => 'i32',
            default => $type->toLLVM(),
        };
    }

    public function createRetVoid(): void
    {
        $this->addLine('ret void', 1);
    }

